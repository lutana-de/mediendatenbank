	public final void updateCount() {
		Integer count = BibDatabase.get().countEntries();
		if (count != null) {
			countNumLbl.setText(count.toString());
		}
		Integer countAudio = BibDatabase.get().countEntries(BibDatabase.Type.AUDIO);
		if (countAudio != null) {
			countNumAudioLbl.setText(countAudio.toString());
		}
		Integer countVideo = BibDatabase.get().countEntries(BibDatabase.Type.VIDEO);
		if (countVideo != null) {
			countNumVideoLbl.setText(countVideo.toString());
		}
	}
	
